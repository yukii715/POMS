    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String originalPassword = txtCurrentPass.getText();
        String newPassword = txtNewPass.getText();
        String confirmPassword = txtConfirmPass.getText();
        String originalPasswordHash = pm.getHash(originalPassword);
        String storedPasswordHash = pm.getPasswordHash();

        if (!originalPasswordHash.equals(storedPasswordHash)) {
            JOptionPane.showMessageDialog(this, "Incorrect Password!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!UserValidation.validPassword(newPassword)) {
            String passwordRequirementsMessage = String.format(
                  "Invalid Password!%n"
                + "Your password must be 8-20 characters long and include%n"
                + "at least one uppercase letter, one lowercase letter,%n"
                + "one number, and one symbol.%n"
                + "Symbols: %s", SecurePassword.SYMBOLS
            );
            JOptionPane.showMessageDialog(this, passwordRequirementsMessage, "Error", JOptionPane.ERROR_MESSAGE);
            return; 
        }

        if (!confirmPassword.equals(newPassword)) {
            JOptionPane.showMessageDialog(this, "Oops! Your Confirm Password doesn't match your New Password.\nPlease re-enter them to ensure they are identical.", "Password Mismatch", JOptionPane.ERROR_MESSAGE);
            return; 
        }

        int result = JOptionPane.showConfirmDialog(this, "Are you sure to change password?", "Password Changing", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (result == JOptionPane.YES_OPTION) {
            pm.changePassword(newPassword);
            JOptionPane.showMessageDialog(this, "Password Changed Successfully!");
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

