    public DateModifier(javax.swing.JDialog parent, boolean modal, PurchaseOrder po) {
        super(parent, modal);
        initComponents();
        
        this.po = po;
        this.previousDate = po.getDeliveryDate();
        setTitle("Extended Delivery Date");
        
        currentYear = po.getDeliveryDate().getYear();
        currentMonth = po.getDeliveryDate().getMonthValue();
        currentDay = po.getDeliveryDate().getDayOfMonth();

        // Populate Year ComboBox (current year to +1)
        cmbYear.removeAllItems();
        for (int i = 0; i <= 1; i++) {
            cmbYear.addItem(String.valueOf(currentYear + i));
        }
        
        if (isInitializing)
        {
            DateResolver.populateCbMonth(cmbYear, cmbMonth, cmbDay, currentYear, currentMonth);
            DateResolver.populateCbDay(cmbYear, cmbMonth, cmbDay, currentYear, currentMonth, currentDay);
        }
        
        cmbYear.setSelectedItem(String.valueOf(currentYear));
        cmbMonth.setSelectedItem(String.format("%02d", currentMonth));
        cmbDay.setSelectedItem(String.format("%02d", currentDay));
        
        isInitializing = false;
    }

