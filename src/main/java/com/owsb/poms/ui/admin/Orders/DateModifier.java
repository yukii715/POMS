    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        int result = JOptionPane.showConfirmDialog(
                this,
                String.format("Are you sure to extend this PO's delivery date?%n%n%s → %s", 
                        previousDate.format(dateFormatter),
                        po.getDeliveryDate().format(dateFormatter)
                ),
                "Extend Delivery Date",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE
            );

            if (result == JOptionPane.YES_OPTION){
                po.setStatus(PurchaseOrder.Status.EXTENDED);
                po.updateStatus();
                JOptionPane.showMessageDialog(this, String.format("Order %s has been Extended", po.getPOID()));
                dispose();
            }
            return;
    }//GEN-LAST:event_btnOKActionPerformed

